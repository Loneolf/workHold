function getMarketString(marketSN, sClass, type) {
  return marketSN + '-' + sClass + '-' + type;
}

/**
 * 可转换企业债券 1-2-6
 * @param marketSN 市场序号
 * @param sClass 股票大类
 * @param type 股票小类
 */
function isSH_CCF(marketSN, sClass, type) {
  const market = getMarketString(marketSN, sClass, type);
  return market === '1-2-6';
}

/**
 * 可转债 0-2-4
 * @param marketSN 市场序号
 * @param sClass 股票大类
 * @param type 股票小类
 */
function isSZ_KZZ(marketSN, sClass, type) {
  const market = getMarketString(marketSN, sClass, type);
  return market === '0-2-4';
}

function isBondType(stockType,stockProp){
  if(!stockType || !stockProp){
    return false
  }
  var market = maket2T3[stockType]
  //获取SECURITYCLASS 30位 大类 SECURITYTYPE  31位小类
  var SECURITYCLASS = stockProp.split('')[30]
  var SECURITYTYPE = stockProp.split('')[31]
  return isSH_CCF(market,SECURITYCLASS,SECURITYTYPE) || isSZ_KZZ(market,SECURITYCLASS,SECURITYTYPE)
}

